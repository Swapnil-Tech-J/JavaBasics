package Operators;

public class Boolean {

	public static void main(String[] args) {
		boolean x = true;
		boolean y = false;
		
		/* For && logical operator If all the operands are true
		then only the operation is true. */
		
		if(x && y) { 
			System.out.println("Condition is true");
		}
		else {
			System.out.println("Condition is false");
		}
	}

}
