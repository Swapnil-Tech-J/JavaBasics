package Operators;

public class Ternary1 {

	public static void main(String[] args) {
		int x;
		
		x= (10 == 9) ? 1:0;
		/*
		 *  1 Indicates true whereas 0 indicates false
		 *  the output will be zero because the condition 
		 *  is false
		 */
		
		System.out.println(x);
	}

}
